#include <stdio.h>
//配列を指すポインタ
//要素を指すポインタ
int main()
{
	//&a;//配列を指すポインタ   int(*)[10] 型
	//&a[0];//要素を指すポインタ int*型
	//a;//要素を指すポインタ（&a[0]） int* 型
	(*(int(*)[3])65536)[0] = -1;//0xFFFFFFFF
	(*(int(*)[3])65536)[1] = -2;//0xFFFFFFFE
	(*(int(*)[3])65536)[2] = -3;//0xFFFFFFFD
	//配列のサイズが持つ意味
	return 0;
}
